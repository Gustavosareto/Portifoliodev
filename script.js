    function startTyping() {
      const output = document.getElementById("code-output");
      let tokenIndex = 0;
      let charIndex = 0;

      function typeNext() {
        if (tokenIndex >= codeTokens.length) return;

        const token = codeTokens[tokenIndex];
        const char = token.text[charIndex];

        let span = output.querySelector(`span[data-token="${tokenIndex}"]`);
        if (!span) {
          span = document.createElement("span");
          span.className = token.cls;
          span.dataset.token = tokenIndex;
          output.appendChild(span);
        }

        span.textContent += char;
        updateLineNumbers();

        charIndex++;

        if (charIndex >= token.text.length) {
          tokenIndex++;
          charIndex = 0;
        }

        setTimeout(typeNext, getDelay(char));
      }

      typeNext();
    }

